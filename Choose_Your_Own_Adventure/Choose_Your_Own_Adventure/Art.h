void print_Mountains() {
	string line, fileName;
	fileName = "art_Files\\mountains.txt";
	ifstream myfile(fileName);
	cout << endl;
	if (myfile.is_open())
	{
		while (getline(myfile, line))
		{
			cout << line << '\n';
		}
		myfile.close();
	}
	else cout << "Unable to find art file" << fileName;
	cout << endl;
}

